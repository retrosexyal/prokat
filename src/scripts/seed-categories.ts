const DRY_RUN = false;

type SeedFaqItem = {
  q: string;
  a: string;
};

type SeedCategoryBaseInput = {
  name: string;
  slug: string;
  seoTitle?: string;
  h1?: string;
  seoDescription?: string;
  introText?: string;
  synonyms?: string[];
  faq?: SeedFaqItem[];
  isActive?: boolean;
  indexingMode?: "index" | "noindex";
  sortOrder?: number;
};

type SeedRootCategoryInput = SeedCategoryBaseInput;

type SeedChildCategoryInput = SeedCategoryBaseInput & {
  parentSlug: string;
};

type CategoryDoc = {
  _id?: ObjectId;
  name: string;
  slug: string;
  parentId: ObjectId | null;
  level: 1 | 2;
  isActive: boolean;
  sortOrder: number;
  indexingMode: "index" | "noindex";
  seoTitle?: string;
  seoDescription?: string;
  h1?: string;
  introText?: string;
  synonyms: string[];
  faq: SeedFaqItem[];
  createdAt: Date;
  updatedAt: Date;
};

const dryRunCategoryMap = new Map<string, CategoryDoc>();

function assertEnv() {
  if (!MONGODB_URI) {
    throw new Error(
      "Не найден MONGODB_URI в env. Проверь .env.local / .env и убедись, что там есть MONGODB_URI.",
    );
  }
}

function normalizeOptionalText(value?: string): string | undefined {
  if (typeof value !== "string") {
    return undefined;
  }

  const normalized = value.trim();
  return normalized || undefined;
}

function normalizeStringArray(values?: string[]): string[] {
  if (!Array.isArray(values)) {
    return [];
  }

  return values.map((item) => String(item).trim()).filter(Boolean);
}

function normalizeFaq(faq?: SeedFaqItem[]): SeedFaqItem[] {
  if (!Array.isArray(faq)) {
    return [];
  }

  return faq
    .map((item) => ({
      q: String(item?.q || "").trim(),
      a: String(item?.a || "").trim(),
    }))
    .filter((item) => item.q && item.a);
}

function fallbackSlugify(value: string): string {
  return String(value || "")
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9а-яё -]/gi, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
}

function buildCategoryDoc(
  input: SeedCategoryBaseInput,
  options: {
    level: 1 | 2;
    parentId: ObjectId | null;
    sortOrder: number;
  },
): Omit<CategoryDoc, "_id" | "createdAt" | "updatedAt"> {
  const { level, parentId, sortOrder } = options;

  const name = String(input.name || "").trim();
  if (!name) {
    throw new Error("У категории отсутствует name");
  }

  const slug = String(input.slug || "").trim() || fallbackSlugify(name);
  if (!slug) {
    throw new Error(`У категории "${name}" не удалось определить slug`);
  }

  return {
    name,
    slug,
    parentId,
    level,
    isActive: typeof input.isActive === "boolean" ? input.isActive : true,
    sortOrder: Number.isFinite(input.sortOrder)
      ? Number(input.sortOrder)
      : sortOrder,
    indexingMode: input.indexingMode === "noindex" ? "noindex" : "index",
    seoTitle: normalizeOptionalText(input.seoTitle),
    seoDescription: normalizeOptionalText(input.seoDescription),
    h1: normalizeOptionalText(input.h1),
    introText: normalizeOptionalText(input.introText),
    synonyms: normalizeStringArray(input.synonyms),
    faq: normalizeFaq(input.faq),
  };
}

async function findCategoryBySlug(
  collection: Collection<CategoryDoc>,
  slug: string,
): Promise<CategoryDoc | null> {
  const normalizedSlug = String(slug).trim();

  if (DRY_RUN) {
    return dryRunCategoryMap.get(normalizedSlug) ?? null;
  }

  return collection.findOne({ slug: normalizedSlug });
}

async function upsertRootCategory(
  collection: Collection<CategoryDoc>,
  input: SeedRootCategoryInput,
  index: number,
): Promise<CategoryDoc | null> {
  const prepared = buildCategoryDoc(input, {
    level: 1,
    parentId: null,
    sortOrder: 100 + index * 10,
  });

  const now = new Date();
  const existing = await findCategoryBySlug(collection, prepared.slug);

  if (existing) {
    const updatedDoc: CategoryDoc = {
      ...existing,
      ...prepared,
      parentId: null,
      level: 1,
      updatedAt: now,
      createdAt: existing.createdAt ?? now,
    };

    if (DRY_RUN) {
      dryRunCategoryMap.set(updatedDoc.slug, updatedDoc);
      console.log(`[DRY RUN] update root: ${prepared.slug}`);
      return updatedDoc;
    }

    await collection.updateOne(
      { _id: existing._id },
      {
        $set: {
          ...prepared,
          parentId: null,
          level: 1,
          updatedAt: now,
        },
      },
    );

    console.log(`updated root: ${prepared.slug}`);
    return collection.findOne({ _id: existing._id });
  }

  const doc: CategoryDoc = {
    _id: new ObjectId(),
    ...prepared,
    createdAt: now,
    updatedAt: now,
  };

  if (DRY_RUN) {
    dryRunCategoryMap.set(doc.slug, doc);
    console.log(`[DRY RUN] create root: ${prepared.slug}`);
    return doc;
  }

  const insertDoc: CategoryDoc = {
    ...prepared,
    createdAt: now,
    updatedAt: now,
  };

  const result = await collection.insertOne(insertDoc);
  console.log(`created root: ${prepared.slug}`);
  return { _id: result.insertedId, ...insertDoc };
}

async function upsertChildCategory(
  collection: Collection<CategoryDoc>,
  input: SeedChildCategoryInput,
  index: number,
): Promise<CategoryDoc | null> {
  const parentSlug = String(input.parentSlug || "").trim();

  if (!parentSlug) {
    throw new Error(
      `У подкатегории "${input.name || input.slug}" отсутствует parentSlug`,
    );
  }

  const parent = await findCategoryBySlug(collection, parentSlug);

  if (!parent) {
    throw new Error(
      `Родительская категория не найдена по slug="${parentSlug}" для подкатегории "${input.name || input.slug}"`,
    );
  }

  if (parent.level !== 1) {
    throw new Error(`Родитель "${parentSlug}" не является root-категорией`);
  }

  const prepared = buildCategoryDoc(input, {
    level: 2,
    parentId: parent._id ?? new ObjectId(),
    sortOrder: 100 + index * 10,
  });

  const now = new Date();
  const existing = await findCategoryBySlug(collection, prepared.slug);

  if (existing) {
    const updatedDoc: CategoryDoc = {
      ...existing,
      ...prepared,
      parentId: parent._id ?? existing.parentId,
      level: 2,
      updatedAt: now,
      createdAt: existing.createdAt ?? now,
    };

    if (DRY_RUN) {
      dryRunCategoryMap.set(updatedDoc.slug, updatedDoc);
      console.log(
        `[DRY RUN] update child: ${prepared.slug} -> parent ${parent.slug}`,
      );
      return updatedDoc;
    }

    await collection.updateOne(
      { _id: existing._id },
      {
        $set: {
          ...prepared,
          parentId: parent._id,
          level: 2,
          updatedAt: now,
        },
      },
    );

    console.log(`updated child: ${prepared.slug} -> parent ${parent.slug}`);
    return collection.findOne({ _id: existing._id });
  }

  const doc: CategoryDoc = {
    _id: new ObjectId(),
    ...prepared,
    parentId: parent._id ?? new ObjectId(),
    createdAt: now,
    updatedAt: now,
  };

  if (DRY_RUN) {
    dryRunCategoryMap.set(doc.slug, doc);
    console.log(
      `[DRY RUN] create child: ${prepared.slug} -> parent ${parent.slug}`,
    );
    return doc;
  }

  const insertDoc: CategoryDoc = {
    ...prepared,
    parentId: parent._id!,
    createdAt: now,
    updatedAt: now,
  };

  const result = await collection.insertOne(insertDoc);
  console.log(`created child: ${prepared.slug} -> parent ${parent.slug}`);
  return { _id: result.insertedId, ...insertDoc };
}

async function printSummary(collection: Collection<CategoryDoc>) {
  const all = DRY_RUN
    ? Array.from(dryRunCategoryMap.values()).sort((a, b) => {
        if (a.level !== b.level) {
          return a.level - b.level;
        }

        if (a.sortOrder !== b.sortOrder) {
          return a.sortOrder - b.sortOrder;
        }

        return a.name.localeCompare(b.name, "ru");
      })
    : await collection.find({}).sort({ level: 1, sortOrder: 1, name: 1 }).toArray();

  console.log("\n=== categories summary ===");

  const roots = all.filter((item) => item.level === 1);

  for (const root of roots) {
    console.log(`\n[ROOT] ${root.name} (${root.slug})`);

    const children = all.filter(
      (item) =>
        item.level === 2 &&
        String(item.parentId) === String(root._id),
    );

    for (const child of children) {
      console.log(`  - [CHILD] ${child.name} (${child.slug})`);
    }
  }

  console.log(`\nTotal categories: ${all.length}`);
}

async function run() {
  assertEnv();

  const client = new MongoClient(MONGODB_URI!);

  try {
    await client.connect();
    const db = DB_NAME ? client.db(DB_NAME) : client.db();
    const collection = db.collection<CategoryDoc>(COLLECTION_NAME);

    console.log(`Using collection: ${COLLECTION_NAME}`);
    console.log(`Database: ${db.databaseName}`);
    console.log(`Roots to process: ${ROOT_CATEGORIES.length}`);
    console.log(`Children to process: ${CHILD_CATEGORIES.length}`);
    console.log(`DRY_RUN: ${DRY_RUN ? "true" : "false"}`);

    if (DRY_RUN) {
      const existing = await collection.find({}).toArray();
      for (const item of existing) {
        dryRunCategoryMap.set(item.slug, item);
      }
    }

    for (let i = 0; i < ROOT_CATEGORIES.length; i += 1) {
      await upsertRootCategory(collection, ROOT_CATEGORIES[i], i);
    }

    for (let i = 0; i < CHILD_CATEGORIES.length; i += 1) {
      await upsertChildCategory(collection, CHILD_CATEGORIES[i], i);
    }

    await printSummary(collection);
    console.log("\nDone.");
  } finally {
    await client.close();
  }
}

run().catch((error) => {
  console.error("\nSeed failed:");
  console.error(error);
  process.exit(1);
});